def is_none(line):
    if line is None:
        return " "
    return line


def redirect(bot, message, id_i_s):
    text = message.text
    if text == "/menu":
        delete_internal_sale(id_i_s)
        menu(bot, message)
        return True
    elif text == "/start":
        delete_internal_sale(id_i_s)
        send_welcome_message(bot, message)
        return True
    return False
